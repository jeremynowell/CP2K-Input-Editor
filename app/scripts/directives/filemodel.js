(function (){
    'use strict';

    /**
     * @ngdoc directive
     * @name cp2kInputEditorApp.directive:fileModel
     * @description
     * Gets the filename from a file element whenever it changes.
     */
    angular.module('cp2kInputEditorApp').directive('fileModel', fileModel);
    fileModel.$inject = ['$parse'];
    function fileModel($parse) {
        return {
            restrict: 'A',
            link: function(scope, element, attrs) {
                var model = $parse(attrs.fileModel);
                var modelSetter = model.assign;

                element.bind('change', function() {
                    scope.$apply(function() {
                        modelSetter(scope, element[0].files[0]);
                    });
                });
            }
        };
    }
})();
